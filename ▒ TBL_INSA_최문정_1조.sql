    150만원 미만 - C등급
    
SELECT BUSEO "부서", ROUND(AVG(BASICPAY+SUDANG)) "평균 등급"
     , CASE WHEN ROUND(AVG(BASICPAY+SUDANG), 2) > 2000000 THEN 'A등급'
            WHEN ROUND(AVG(BASICPAY+SUDANG), 2) >= 1500000 AND ROUND(AVG(BASICPAY+SUDANG)) <= 2000000 THEN 'B등급'
            WHEN ROUND(AVG(BASICPAY+SUDANG), 2) < 1500000 THEN 'C등급'
            ELSE '등급판정불가'
       END "등급"
FROM TBL_INSA
GROUP BY BUSEO;
