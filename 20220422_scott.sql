    UPDATE TBL_상품
    SET 재고수량 = V_재고수량 - V_삭제할입고수량
    WHERE 상품코드 = V_상품코드;

    -- 커밋
    COMMIT;

    -- 예외 처리
    EXCEPTION
        WHEN USER_DEFINE_ERROR
            THEN RAISE_APPLICATION_ERROR(-20003, '삭제할 입고번호의 입고수량보다 현재 재고수량이 적습니다.');
