    SELECT 입고수량 INTO V_삭제할입고수량
    FROM TBL_입고
    WHERE 입고번호 = V_입고번호;

    -- V_상품코드 변수에 값 담아내기
    SELECT 상품코드 INTO V_상품코드
    FROM TBL_입고
    WHERE 입고번호 = V_입고번호;

     -- V_재고수량 변수에 값 담아내기
    SELECT 재고수량 INTO V_재고수량
    FROM TBL_상품
    WHERE 상품코드 = V_상품코드;

