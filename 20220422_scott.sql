  CREATE OR REPLACE TRIGGER "SCOTT"."TRG_IBGO" 
        AFTER 
        INSERT OR UPDATE OR DELETE ON TBL_입고
        FOR EACH ROW
BEGIN

    IF (INSERTING)
        THEN UPDATE TBL_상품
            SET 재고수량 = 재고수량 + :NEW.입고수량
